"use strict";

/*
Необходимо попросить пользователя ввести три числа.
Необходимо создать функцию, в которую мы должны передать эти три числа.
Функция должна определить максимальное, среди переданных ей значение и 
вывести сообщение: "Максимальное значение среди чисел N1, N2, N3 равно N."

Примечание: Условимся, что пользователь всегда вводит корректные значения, 
три числа. Проверять их не нужно.
*/

// Функция для определения максимального значения среди трех чисел

const findMax = (num1, num2, num3) => {

    // Находим максимальное значение с помощью Math.max()

    const max = Math.max(num1, num2, num3);
  
    // Выводим сообщение с максимальным значением

    console.log(`Максимальное значение среди чисел ${num1}, ${num2}, ${num3} равно ${max}.`);
  };
  
  // Запрашиваем у пользователя три числа

  const num1 = prompt("Введите первое число:");
  const num2 = prompt("Введите второе число:");
  const num3 = prompt("Введите третье число:");
  
  // Вызываем функцию findMax с переданными числами
  
  findMax(num1, num2, num3);
